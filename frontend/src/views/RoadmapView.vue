    refetchDebounce = null
    // Don't stomp an in-flight optimistic reorder — re-arm and try again once
    // the PATCH settles.
    if (isPersisting.value) {
      onRoadmapUpdated()
      return
    }
    await fetchRoadmap()
    dismissWaiting()
  }, REFETCH_DEBOUNCE_MS)
}

