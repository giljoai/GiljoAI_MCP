    await projectStore.activateProject(item.project_id)
    const project = projectStore.projects.find((p) => p.id === item.project_id)
    const staged =
      project &&
      (project.staging_status === 'staged' || project.staging_status === 'staging_complete')
    router.push({
      name: 'ProjectLaunch',
      params: { projectId: item.project_id },
      query: { via: 'jobs', ...(staged ? { tab: 'jobs' } : {}) },
    })
  } catch (error) {
    console.error('[ROADMAP] Failed to activate project:', error)
    showToast({ message: 'Failed to activate the project. Please try again.', type: 'error' })
  }
}

