onMounted(async () => {
  // Live refresh: the agent's roadmap write broadcasts roadmap:updated; re-fetch
  // and clear the waiting indicator. Direct wsStore subscription (same pattern as
  // ActiveProductDisplay.vue) — RoadmapView owns view-local state, no store.
  unsubRoadmap = wsStore.on('roadmap:updated', onRoadmapUpdated)
  // FE-6240: the agent's first roadmap-tool touch (MCP get_roadmap) broadcasts
  // roadmap:agent_active — raise the waiting spinner on the real agent
  // connection (replaces the old optimistic copy-click trigger).
  unsubAgentActive = wsStore.on('roadmap:agent_active', onAgentActive)
  // Live-sync: a project changed OUTSIDE the roadmap (project list, agent) fires
  // a project_update WS event — status_changed/activated/deactivated (status) or
  // updated (rename/alias, bug-2). Without this subscription the card shows a
  // stale status/title until the user navigates away and back (router
  // force-remount). Reuse the debounced fetchRoadmap — identical mechanism to
  // the roadmap:updated handler above.
  unsubProjectUpdate = wsStore.on('project_update', onProjectUpdated)
  // FE-6165f: hydrate the active-chain set so "In chain" state is available at
  // mount. Re-hydrate on WS reconnect so a run that finished while disconnected
  // unlocks immediately instead of waiting for the next WS event.
  unsubResync = registerReconnectResync(() => sequenceRunStore.hydrate())
  try {
    await Promise.all([productStore.fetchActiveProduct(), fetchRoadmap(), sequenceRunStore.hydrate()])
    // TSK-6243: product id is known now — re-show the spinner if an agent was
    // active within the safety window just before this reload.
    rehydrateWaitingFromStorage()
    try {
      const { data } = await api.taxonomyTypes.list()
      projectTypes.value = data || []
    } catch {
      console.error('[ROADMAP] Failed to load taxonomy types')
    }
  } catch (error) {
    console.error('[ROADMAP] Failed to initialize:', error)
  }
})

onUnmounted(() => {
  if (unsubRoadmap) unsubRoadmap()
  if (unsubAgentActive) unsubAgentActive()
  if (unsubProjectUpdate) unsubProjectUpdate()
  if (unsubResync) unsubResync()
  if (waitingTimeout) clearTimeout(waitingTimeout)
  if (refetchDebounce) clearTimeout(refetchDebounce)
})

