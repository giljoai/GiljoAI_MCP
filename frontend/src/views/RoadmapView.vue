// Clipboard write with a secure-context fallback (LAN HTTP has no
// navigator.clipboard). Always wrapped so a failure never throws into the UI.
async function copyToClipboard(text) {
  try {
    if (navigator.clipboard && window.isSecureContext) {
      await navigator.clipboard.writeText(text)
      return true
    }
    throw new Error('insecure-context')
  } catch {
    try {
      const ta = document.createElement('textarea')
      ta.value = text
      ta.style.position = 'fixed'
      ta.style.opacity = '0'
      document.body.appendChild(ta)
      ta.focus()
      ta.select()
      const ok = document.execCommand('copy')
      document.body.removeChild(ta)
      return ok
    } catch {
      return false
    }
  }
}

async function copyRoadmapPrompt() {
  const mode = isEmptyRoadmap.value ? 'create' : 'refresh'
  // FE-6240: copy the user's edited prompt when "Add my own instructions" is
  // on, otherwise the generated one. The waiting spinner is NO LONGER raised
  // here — it is raised by the roadmap:agent_active WS signal the moment the
  // agent actually touches the roadmap tool, not on this copy click.
  const text = customPromptEnabled.value ? customPromptText.value : buildRoadmapPrompt(mode)
  const ok = await copyToClipboard(text)
  if (ok) {
    showToast({
      message: 'Prompt copied — paste it into the agent connected to THIS account.',
      type: 'success',
    })
  } else {
    showToast({
      message: 'Could not copy automatically. Select and copy the prompt manually.',
      type: 'warning',
    })
  }
}

// --- Fetch ---
async function fetchRoadmap() {
  loading.value = true
  try {
    const { data } = await api.roadmap.get()
    noActiveProduct.value = false
    roadmap.value = data.roadmap || null
    items.value = Array.isArray(data.items) ? [...data.items] : []
  } catch (error) {
    if (error.response?.status === 404) {
      noActiveProduct.value = true
      roadmap.value = null
      items.value = []
    } else {
      console.error('[ROADMAP] Failed to load roadmap:', error)
      showToast({ message: 'Failed to load the roadmap. Please try again.', type: 'error' })
    }
  } finally {
    loading.value = false
  }
}

// --- vuedraggable reorder (drag scoped to the .rm-grip handle) ---
// vuedraggable hands back the reordered VISIBLE list. Because "Fold in tasks" is
// a view-only filter, rebuild the FULL items order from that visible order —
// folded-out tasks keep their absolute slots — then persist over the whole set.
function rebuildFullOrder(newVisibleOrder) {
  const visibleIds = new Set(displayItems.value.map((i) => i.id))
  let vi = 0
  return items.value.map((it) => (visibleIds.has(it.id) ? newVisibleOrder[vi++] : it))
}

function onListReorder(newVisibleOrder) {
  persistOrder(rebuildFullOrder(newVisibleOrder))
}

async function demote(item) {
  const arr = [...items.value]
  const from = arr.findIndex((i) => i.id === item.id)
  if (from < 0 || from === arr.length - 1) return
  const [moved] = arr.splice(from, 1)
  arr.push(moved)
  await persistOrder(arr)
}

// Recompute sort_order = position, PATCH the whole (small) list. Idempotent.
async function persistOrder(orderedArr) {
  const previous = items.value
  items.value = orderedArr // optimistic
  isPersisting.value = true
  try {
    const payload = orderedArr.map((it, i) => ({ id: it.id, sort_order: i }))
    await api.roadmap.reorder(payload)
  } catch (error) {
    console.error('[ROADMAP] Failed to persist new order:', error)
    items.value = previous // rollback
    showToast({ message: 'Could not save the new order. Please try again.', type: 'error' })
    await fetchRoadmap()
  } finally {
    isPersisting.value = false
  }
}

// FE-6180: /roadmap is plan+launch only — it performs NO chain management. An
// in-chain tickbox is a disabled indicator (toggle never fires for it); a non-chain
// row toggles selection normally.
function handleRoadmapToggle(item, toggle) {
  const projectId = item.project_id || item.id
  if (!projectId) return
  if (!sequenceRunStore.isProjectInActiveChain(projectId)) toggle(item)
}

// FE-6180: clicking a linked (greyed) tickbox on /roadmap sends the user to /projects,
// where chain reset/modify lives. Deep-link to the chain head with ?run= when resolvable.
function openChainOnProjects(item) {
  const projectId = item.project_id || item.id
  const run = projectId ? sequenceRunStore.runForProject(projectId) : null
  if (run) {
    const head = run.resolved_order?.[0] || run.project_ids?.[0]
    if (head) {
      router.push({ name: 'ProjectLaunch', params: { projectId: head }, query: { run: run.id } })
      return
    }
  }
  router.push({ name: 'Projects' })
}

// FE-6022c: optimistic remove → DELETE roadmap_item; rollback on failure.
async function removeItem(item) {
  if (!item?.id) return
  const previous = items.value
  items.value = items.value.filter((it) => it.id !== item.id)
  try {
    await api.roadmap.removeItem(item.id)
    showToast({ message: 'Removed from roadmap', type: 'success' })
  } catch (error) {
    console.error('[ROADMAP] Failed to remove item:', error)
    items.value = previous // rollback
    showToast({ message: 'Could not remove from the roadmap. Please try again.', type: 'error' })
  }
}

